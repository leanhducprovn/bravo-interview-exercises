function success(status, messenger) {
    Swal.fire(
        status,
        messenger,
        'success'
    )
}

function error(status, messenger) {
    Swal.fire(
        status,
        messenger,
        'error'
    )
}

function warning(status, messenger) {
    Swal.fire(
        status,
        messenger,
        'warning'
    )
}
